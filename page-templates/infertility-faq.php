		<br> 4. It’s also possible that the uterus might not be receptive to implantation due to abnormal conditions like polyps, fibroids or infections.
		<br><br>It’s important to note that not all causes are due to female factors. In fact, 30-40% of infertility cases are due to male factors. We need enough motile, swimming sperm to fertilize an egg. A “normal” ejaculate has over 18 million motile (swimming) sperm. Some men produce fewer or no sperm. Other men produce plenty of sperm but have obstructions in their tubes (the vas deferens), preventing sperm from being released. Just because a semen sample appears normal, doesn’t mean that there are sufficient numbers of sperm inside. We need to check with a microscope.</p>
