		<p>You do not need to transfer every embryo. After you transfer your desired and recommended embryo(s) we can cryopreserve any additional embryos with the potential for implanting. These are saved for a future pregnancy or if this attempt isn’t successful. Embryologists have been cryopreserving embryos since the 1980s. Today, embryo survival is better than 98% and many studies have shown the outcomes with frozen embryo transfers to be as good or better than with fresh embryos.</p>
	  </div>
	</div>
  </div>
  <div class="panel">
	<div class="panel-heading" role="tab" id="h-13">
		<h3><span class="glyphicon glyphicon-menu-right" aria-hidden="true"></span><a role="button" data-toggle="collapse" data-parent="#medical" href="#c-13" aria-expanded="true" aria-controls="c-13">
			 What is elective cryopreservation: Are Frozen Embryos better than Fresh?</a>
		</h3>
	</div>
	<div id="c-13" class="panel-collapse collapse" role="tabpanel" aria-labelledby="h-13">
	  <div class="panel-body">
		<p> Several recent studies have been published that suggest frozen embryos may have higher pregnancy and live birth rates and may even have better live birth outcomes. Our interpretation: For some patients a frozen embryo transfer is going to be safer and result in a higher pregnancy rate. However, not all patients benefit from this approach so we do not recommend freezing all embryos to every patient. We believe we can identify the patients who will benefit from a “freeze all” approach and frozen embryo transfer during their monitoring. In these cases, we recommend cryopreserving all of your embryos without transferring them in order to increase your chance of a successful and healthy pregnancy.
		<br><br> <strong>Reasons we might cryopreserve without transferring an embryo:</strong>
		<br><br> <em> <strong>Elevated early progesterone.</strong> The uterus has a “window of receptivity” when it will allow a good embryo to implant.  Before and after that window, even a good embryo won’t be successful. Sometimes, the elevated hormone levels seen with ovarian stimulation can “confuse” the uterus into thinking the embryo will be arriving before it is ready. By the time the embryos arrive, the uterus is not receptive to implantation. So, instead of transferring a good embryo into a uterus that is no longer “receptive,” we’ll wait and use a frozen embryo when the timing is better <br> <strong>New concerns about the uterine lining.</strong> Rarely, we detect a new polyp or bleeding that was missed before starting the IVF process.  <br> <strong>Ovarian hyperstimulation syndrome.</strong> In patients who produce many eggs and embryos, ovaries can become hyperstimulated and need time to ‘cool down’ to allow for successful implantation. This is quite rare, and although it’s disappointing to experience an unplanned delay, it usually means there’s a good chance at a successful pregnancy the next month with a frozen embryo transfer.  <br> <em>*Patient choice.</em></em> One of the most common reasons is that a patient or couple wants to create embryos to transfer later, either for medical or personal reasons. </p>
	  </div>
	</div>
  </div>
  <div class="panel">
	<div class="panel-heading" role="tab" id="h-14">
		<h3><span class="glyphicon glyphicon-menu-right" aria-hidden="true"></span><a role="button" data-toggle="collapse" data-parent="#medical" href="#c-14" aria-expanded="true" aria-controls="c-14">
			 How long can my embryos be frozen?</a>
		</h3>
	</div>
	<div id="c-14" class="panel-collapse collapse" role="tabpanel" aria-labelledby="h-14">
	  <div class="panel-body">
		<p>Embryo freezing has been performed since the 1980s. Sperm cryopreservation has been successfully performed since the 1970s. We have seen many births from embryos cryopreserved for over 10 years and even some births from sperm cryopreserved for as long as 19 years. Animal studies suggest that cryopreservation is possible for even longer without detectable consequences but beyond 10-15 years, our experience is limited.</p>
	  </div>
	</div>
  </div>
  <div class="panel">
	<div class="panel-heading" role="tab" id="h-15">
		<h3><span class="glyphicon glyphicon-menu-right" aria-hidden="true"></span><a role="button" data-toggle="collapse" data-parent="#medical" href="#c-15" aria-expanded="true" aria-controls="c-15">
			 For frozen transfers, why might someone do a “controlled” (medicated) versus “natural” transfer cycle?</a>
		</h3>
	</div>
	<div id="c-15" class="panel-collapse collapse" role="tabpanel" aria-labelledby="h-15">
	  <div class="panel-body">
		<p>One decision we’ll consider together is whether you’ll want to do a natural or controlled (medicated) frozen embryo transfer.
		<br><br> If you’ve decided to get genetic testing or are otherwise using frozen embryos, then we’ll work with you to time your transfer. Compared to a “fresh” IVF cycle with ovarian stimulation, a frozen embryo transfer is much easier. Depending on your preferences for timing and your medical history, we can do a controlled cycle or a natural cycle.
		<br><br> <strong>Controlled Cycle:</strong> We give you medications to prevent your body from ovulating. We provide all the estrogen and progesterone you need for about 10 weeks.
		<br> <strong>Advantages:</strong> You can schedule the transfer to your convenience and have more control over the process.
		<br> <strong>Disadvantages:</strong> Cost and inconvenience of taking medications for 10 weeks.
		<br><br> <strong>Natural Cycle:</strong> We allow your body to ovulate on its own. As a result, you produce your own estrogen and progesterone and require minimal additional medication.
		<br> <strong>Advantages:</strong> Lower cost of medication.
		<br><em> <em>*Disadvantages:</em></em> There is more initial monitoring to make sure we know when you ovulate and as such, it’s more unpredictable. We cannot control the exact date of the embryo transfer, since your body will ultimately determine when we need to transfer the embryo. We are also unable to give supplemental estradiol before you ovulate, so patients with thin lining may do better with a controlled cycle to supplement endometrial growth.</p>
	  </div>
	</div>
  </div>
  <div class="panel">
	<div class="panel-heading" role="tab" id="h-16">
		<h3><span class="glyphicon glyphicon-menu-right" aria-hidden="true"></span><a role="button" data-toggle="collapse" data-parent="#medical" href="#c-16" aria-expanded="true" aria-controls="c-16">
			 How common is miscarriage? What are my chances once I’m pregnant?</a>
		</h3>
	</div>
	<div id="c-16" class="panel-collapse collapse" role="tabpanel" aria-labelledby="h-16">
	  <div class="panel-body">
		<p>Unfortunately, miscarriages are very common and related to the age of the woman whose eggs are being used. While only 10-15% of pregnancies in your 20’s will end in miscarriage, this risk increases substantially during your mid to late 30’s. For women over 40 years of age over half of pregnancies will end in miscarriage. By age 45, nearly 75% of pregnancies will end in miscarriage.</p>
	  </div>
	</div>
  </div>
  <div class="panel">
	<div class="panel-heading" role="tab" id="h-17">
		<h3><span class="glyphicon glyphicon-menu-right" aria-hidden="true"></span><a role="button" data-toggle="collapse" data-parent="#medical" href="#c-17" aria-expanded="true" aria-controls="c-17">
			 Can I prevent a miscarriage?</a>
		</h3>
	</div>
	<div id="c-17" class="panel-collapse collapse" role="tabpanel" aria-labelledby="h-17">
	  <div class="panel-body">
		<p>While IVF alone cannot prevent a miscarriage, it does allow for embryo testing, which can minimize the risk of miscarriage after embryo transfer. After embryo sampling and PGS, the subsequent miscarriage rate is very low (3-8%) if the embryo has been tested and shown to be euploid.</p>
	  </div>
	</div>
  </div>
  <div class="panel">
	<div class="panel-heading" role="tab" id="h-18">
		<h3><span class="glyphicon glyphicon-menu-right" aria-hidden="true"></span><a role="button" data-toggle="collapse" data-parent="#medical" href="#c-18" aria-expanded="true" aria-controls="c-18">
			 What if I’ve had an unsuccessful IVF cycle?</a>
		</h3>
	</div>
	<div id="c-18" class="panel-collapse collapse" role="tabpanel" aria-labelledby="h-18">
	  <div class="panel-body">
		<p>We are definitely here to help. We offer complimentary consultations for patients who have had unsuccessful experience with IVF. Just send us your records and book a consult with our team. Our doctors and embryologists will review your cycle before you arrive and give you our recommendations. In order to give you the best advice, we will want to see and evaluate both your ovarian stimulation and monitoring records as well as the embryology lab reports.
		<br><br> At Spring Fertility, we are grateful to have achieved pregnancies in some of the most challenging cases. Nearly every patient with a disappointing outcome elsewhere has had a pregnancy or improved IVF outcome here. If we don’t think we can help, we’ll let you know.</p>
	  </div>
